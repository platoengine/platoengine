    tGoldControl(0,2) = 0.56692565516265081;
    tGoldControl(0,3) = 0.28805773602398665;
    // GOLD PROBABILITIES
    tGoldControl(1,0) = 0.24979311097918996;
    tGoldControl(1,1) = 0.24999286050154013;
    tGoldControl(1,2) = 0.25018122221660277;
